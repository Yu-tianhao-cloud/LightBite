def seed(db):
    existing = db.query(Recipe).filter(Recipe.category == "ingredient").first()
    if existing:
        print(f"食材已存在，跳过导入。已有 {db.query(Recipe).filter(Recipe.category == 'ingredient').count()} 种食材。")
        return

    count = 0
    for name, servings, kcal, protein, carbs, fat, tags in INGREDIENTS:
        recipe = Recipe(
            name=name,
            category="ingredient",
            default_servings=servings,
            total_grams=servings,  # for ingredients, default_servings IS the gram weight
            total_calories=kcal,
            total_protein_grams=protein,
            total_carbs_grams=carbs,
            total_fat_grams=fat,
            difficulty="easy",
            cooking_time_minutes=5,
            tags=tags,
        )
        db.add(recipe)
        count += 1

    db.commit()
    print(f"Done: {count} ingredients imported.")


if __name__ == "__main__":
    db = SessionLocal()
    try:
        seed(db)
    finally:
        db.close()
